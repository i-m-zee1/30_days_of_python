import sys
import platform

print("Python version: ",sys.version) # Full version string with sys import
print("Python version: ",sys.version_info)    # Tuple with major, minor, micro sys import

print("Python version:", platform.python_version()) # Full version string with platform import
print("Version tuple:", platform.python_version_tuple())  # Tuple with major, minor, micro with platform import


